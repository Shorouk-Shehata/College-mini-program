    private void button1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button1ActionPerformed
        // TODO add your handling code here:
        if (!jTextFieldId2.getText().equals("")      &&
            !jTextFieldFirst.getText().equals("")   &&
            !jTextFieldLast.getText().equals("")    &&
            !jTextFieldPhone.getText().equals("")   &&
            !jTextFieldEmail.getText().equals("")   &&
            !jTextFieldPassword.getText().equals("") &&   
            (Male.isSelected() || Female.isSelected())        ){

            Project.Admin x = new Project.Admin();

            x.setId(Integer.parseInt(jTextFieldId2.getText()));
            x.setFirstName(jTextFieldFirst.getText());
            x.setLastName(jTextFieldLast.getText());
            x.setPhone(jTextFieldPhone.getText());
            x.setEmail(jTextFieldEmail.getText());
            x.setPassword(jTextFieldPassword.getText());
              if (Male.isSelected())
                x.setGender(Male.getText());
              if (Female.isSelected())
                x.setGender(Female.getText());   


            if (x.updateAdmin()){
                Message.setText("Done");
                resetPanelData();
            }
            else {
                Message.setText("Failed");
            }
        }
        else {
            Message.setText("Please Fill Fields");

        }        
        
        
              
    }//GEN-LAST:event_button1ActionPerformed

