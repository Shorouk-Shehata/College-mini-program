    private void LecturerButton_AdminActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LecturerButton_AdminActionPerformed
        // TODO add your handling code here:
        
        if (!jTextFieldId.getText().equals("")){
            Project.Admin x =new Project.Admin();
            Project.Admin returned = x.searchAdminById(Integer.parseInt(jTextFieldId.getText()));
            if (returned.getId() > 0 ){
                setPanelData(returned);
            }
            else {
                Message.setText("Not Found");
            }

        }
        else {
            Message.setText("Please Enter ID");
        }        
    }//GEN-LAST:event_LecturerButton_AdminActionPerformed

