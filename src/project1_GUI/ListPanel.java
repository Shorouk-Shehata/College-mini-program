        public void addRowsTojTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Object rowData[] = new Object[7];

        Project.Exam s = new Project.Exam();
        ArrayList<Project.Exam> exams = new ArrayList<Project.Exam>();

        exams = s.listExams();

        for (Project.Exam x : exams) {
            rowData[0] = x.getQNumber();
            rowData[1] = x.getQuestion();
            rowData[2] = x.getCorrect();


            model.addRow(rowData);
        }
    
        }
    
    
    
